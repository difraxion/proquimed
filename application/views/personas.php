        <p>Los virus del papiloma humano (VPH), son un grupo de más de 100 
            tipos de virus. Se les llama papilomavirus porque algunos tipos 
            pueden causar verrugas o papilomas. Cada tipo de VPH se le identifica 
            con número, por ejemplo 6, 11, 16, 18, etc. Los VPH que causan las 
            verrugas comunes que crecen en las manos y en los pies son diferentes 
            de los que causan en cáncer cervico-uterino. </p>

