                PRUEBA PRE-CLÍNICA DE PREECLAMPSIA </span>
        </header>
        <p>La preeclampsia se presenta en el 8% de todos los embarazo 
            en México y puede ocasionar daños graves a la salud de la madre 
            y del bebé. 2 de de cada 3 embazadas que desarrollarán formas graves 
            de preeclampsia lo hacen inesperadamente, porque no tienen factores de 
            riesgo evidentes al control prenatal. </p>

        <p>
            La prueba se basa en el concepto de balance entre 
            un marcador anti-angiogénico (sFlt-1) y otro pro-angiogénico 
            (PIGF), producido por la placenta; un cociente (sFlt-1/PIGF) 
             elevado es anormal, se interpreta como positivo e indica anticipadamente
              que el embarazo desarrollará preeclamsia clínica ( hipertensión y 
              proteinuria) dentro de las siguientes 4 - 6 semanas ( sensibilidad 
              - 89-100% especificidad 95%-97%)
        </p>
